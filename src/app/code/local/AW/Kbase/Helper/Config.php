class AW_Kbase_Helper_Config extends Mage_Core_Helper_Abstract {
    # URL Rewrite tab
        # Secure URL
        const CONFIG_URLREWRITE_SECUREURLS = 'kbase/url_rewrite/secure_urls';
        
    # URL Rewrite tab
    public function getUrlRewriteUseSecureUrls($store = null) {
        return Mage::getStoreConfig(self::CONFIG_URLREWRITE_SECUREURLS, $store) ? true : false;
    }
}
